export function localizedCompetitionName(competitionId: string, fallback: string, locale: Locale): string {
  const m = COMPETITION_NAME_MAP[competitionId];
  if (m) return m[locale];
  return fallback;
}

export function localizedCompetitionShortName(competitionId: string, fallback: string, locale: Locale): string {
  const m = COMPETITION_SHORT_NAME_MAP[competitionId];
  if (m) return m[locale];
  return fallback;
}

export function localizedTeamName(name: string | null | undefined, locale: Locale): string {
  if (!name) return '';
  const m = TEAM_NAME_MAP[name];
  if (m) return m[locale];
  return name;
}

export function localizedTeamFromId(teamId: string, locale: Locale): string {
  // strip prefix like "club:global:" or "nat:" → readable
  const tail = teamId.replace(/^[^:]+:[^:]+:/, '').replace(/^[^:]+:/, '');
  const readable = tail.replaceAll('_', ' ').replace(/\b\w/g, (c) => c.toUpperCase());
  return localizedTeamName(readable, locale);
}

export function format(s: string, params: Record<string, string | number>): string {
  return s.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`));
}
